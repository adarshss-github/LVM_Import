function [D] = lvmImport(Fn,del,lskp)

%===========================================================================================================================================================
%***********************************************************************************************************************************************************
%
%                                      --------------------------------------------------
%                                                          PREAMBLE
%                                      --------------------------------------------------
%
%[D] = lvmImport(Fn,del,lskp)
%
%Adarsh S, Ph.D. Candidate, IIT Kanpur
%
%Description:
%-----------
%Imports data from a file
%
%Input arguments:
%---------------
%1)Fn: File name
%2)del: Delimeter to be considered
%3)lskp: Number of lines to skip
%
%Output arguments:
%----------------
%1)D: Data in matrix form
%
%
%Ex:  [D] = lvmImport('Data',',',6) ;

%
%
%
%                                      -----------------------------------------------
%                            *********|| All rights reserved; Adarsh S; November, 2017 || *********
%                                      -----------------------------------------------
%
%
%***********************************************************************************************************************************************************
%===========================================================================================================================================================



[A] = importdata(Fn,del,lskp) ;
D = A.data ;
clear A

end